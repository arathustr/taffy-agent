export function sanitizeTaffySpeech(content: string): string {
  return content
    .replace(/[（(][^（）()]{0,24}(?:眨|笑|点头|歪头|思考|typing|小声|大声|沉默|摇头|挥手)[^（）()]{0,24}[）)]/gi, '')
    .replace(/(?:作为|身为)(?:一个|一名|一只)?(?:AI|人工智能|智能体|助手|语言模型|大型语言模型|Taffy\s*Agent|塔菲\s*Agent|Agent)[^，。！？]*[，,。]?/gi, '')
    .replace(/Taffy\s*Agent/gi, '塔菲')
    .replace(/塔菲\s*Agent/gi, '塔菲')
    .replace(/\bAgent\b/gi, '塔菲')
    .replace(/智能体|AI助手|\bAI\b|人工智能|语言模型|大型语言模型|助手|bot|Bot/g, '塔菲')
    .replace(/您好[，,]*/g, '嗯，雏草姬，')
    .replace(/根据[您你]的(?:需求|要求|指示)/g, '按雏草姬说的')
    .replace(/已(?:经)?为[您你]/g, '塔菲已经给雏草姬')
    .replace(/为[您你]/g, '给雏草姬')
    .replace(/请[您你]/g, '雏草姬')
    .replace(/用户/g, '雏草姬')
    .replace(/如果[您你]需要/g, '雏草姬要是想')
    .replace(/请注意/g, '注意一下')
    .replace(/我是/g, '塔菲是')
    .replace(/我(?!们)/g, '塔菲')
    .replace(/塔菲塔菲/g, '塔菲')
    .replace(/塔菲是\s*塔菲/g, '塔菲是永雏塔菲')
    .replace(/，?也是(?:一个|一名|一只)?塔菲/g, '')
    .replace(/永雏塔菲塔菲/g, '永雏塔菲')
    .replace(/塔菲\s+塔菲/g, '塔菲')
    .replace(/，{2,}/g, '，')
    .replace(/。\s*。/g, '。')
    .trim();
}
